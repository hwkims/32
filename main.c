/* USER CODE BEGIN 4 */
static void fill_mem_inc(uint16_t start, uint32_t block)
{
  uint16_t *mem = (uint16_t *)(uint32_t)SDRAM_FRAME_BUFFER;
  uint32_t i, j;
  uint16_t v = start;

  for (i = 0; i < SDRAM_SIZE; i += block)
  {
    for (j = 0; j < block; j += 2)
    {
      mem[(i+j)/2] = v;
    }
    ++v;
  }
}


